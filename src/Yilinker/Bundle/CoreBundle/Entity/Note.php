<?php

namespace Yilinker\Bundle\CoreBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Note
 */
class Note
{
   
    /**
     * @var integer
     */
    private $noteId;

    /**
     * @var string
     */
    private $note;

    /**
     * @var \DateTime
     */
    private $dateAdded;

    /**
     * @var \DateTime
     */
    private $lastDateModified;


    /**
     * Get noteId
     *
     * @return integer 
     */
    public function getNoteId()
    {
        return $this->noteId;
    }

    /**
     * Set note
     *
     * @param string $note
     * @return Note
     */
    public function setNote($note)
    {
        $this->note = $note;

        return $this;
    }

    /**
     * Get note
     *
     * @return string 
     */
    public function getNote()
    {
        return $this->note;
    }

    /**
     * Set dateAdded
     *
     * @param \DateTime $dateAdded
     * @return Note
     */
    public function setDateAdded($dateAdded)
    {
        $this->dateAdded = $dateAdded;

        return $this;
    }

    /**
     * Get dateAdded
     *
     * @return \DateTime 
     */
    public function getDateAdded()
    {
        return $this->dateAdded;
    }

    /**
     * Set lastDateModified
     *
     * @param \DateTime $lastDateModified
     * @return Note
     */
    public function setLastDateModified($lastDateModified)
    {
        $this->lastDateModified = $lastDateModified;

        return $this;
    }

    /**
     * Get lastDateModified
     *
     * @return \DateTime 
     */
    public function getLastDateModified()
    {
        return $this->lastDateModified;
    }
}
