<?php

namespace Yilinker\Bundle\CoreBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * DisputeStatusType
 */
class DisputeStatusType
{

    const STATUS_TYPE_OPEN = 1;

    const STATUS_TYPE_CLOSE = 2;

    /**
     * @var integer
     */
    private $disputeStatusTypeId;

    /**
     * @var string
     */
    private $name;

    /**
     * Get disputeStatusTypeId
     *
     * @return integer 
     */
    public function getDisputeStatusTypeId()
    {
        return $this->disputeStatusTypeId;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return DisputeStatusType
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

}
